public class math {
    public static void main(String[] args) {
        int a = 10;
        int b = 100;
        System.out.println("Max of "+a+" and "+b+" is "+Math.max(a, b));
        System.out.println("Min of "+a+" and "+b+" is "+Math.min(a, b));
        System.out.println("Square Root of "+b+" is "+Math.sqrt(b));
        System.out.println("Power of "+a+" to 2 is "+Math.pow(a, 2));
        // System.out.println("Average of "+a+" and "+b+" is "+Math.Average(a,b));
        System.out.println("absolute of -"+a+" is "+Math.abs(-a));
    }
}
